package fi.tuni.trailweather;

import java.io.IOException;
import javafx.application.Application;
import javafx.application.Platform;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

/**
 * JavaFX App
 */
public class App extends Application {

    private static Scene scene;

    @Override
    public void start(Stage stage) throws IOException {
        new UserData();
        Scene scene = new Scene(loadFXML("/fxml/map"));
        // Add stylesheet to the scene
        scene.getStylesheets().add(getClass().getResource("/styles/map.css").toExternalForm());
        stage.setTitle("TrailWeather");
        stage.onCloseRequestProperty().setValue(e -> Platform.exit());
        stage.setScene(scene);
        stage.show();
    }

    static void setRoot(String fxml) throws IOException {
        scene.setRoot(loadFXML(fxml));
    }

    private static Parent loadFXML(String fxml) throws IOException {
        FXMLLoader fxmlLoader = new FXMLLoader(App.class.getResource(fxml + ".fxml"));
        return fxmlLoader.load();
    }

    public static void main(String[] args) {
        launch();
    }
}
